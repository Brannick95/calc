public class calculator {
    int num1;
    int num2;

    public int add() {
        return num1 + num2;
    }

    public int sub() {
        return num1 - num2;
    }

    public int mult() {
        return num1 * num2;
    }

    public int div() {
        return num1 / num2;
    }

    public int sqr() {
        return num1 * num1;
    }
}
